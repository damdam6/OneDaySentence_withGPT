<script setup>
import { ref } from 'vue';

// 반응형 상태로서 sentence 내용을 보여줄지 결정하는 상태
const showSentence = ref(false);

// showSentence 상태를 토글하는 함수
const toggleSentence = () => {
  if(showSentence.value)return;
  showSentence.value = !showSentence.value;
};
</script>



<template>
  <div class="item" @click="toggleSentence">
    <i>
      <slot name="icon"></slot>
    </i>
    <div class="details">
      <h3>
        <slot name="heading"></slot>
      </h3>
      <!-- showSentence가 true일 때만 sentence 슬롯의 내용을 보여줌 -->
      <div v-if="showSentence">
        <slot name="sentence"></slot>
      </div>
    </div>
  </div>
</template>

